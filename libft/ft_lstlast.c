#include "libft.h"

t_list	*ft_lstlast(t_list *lst)
{
	t_list	*tmp;

	if (!lst)
		return (NULL);
	while (lst != 0)
	{
		tmp = lst;
		lst = lst->next;
	}
	return (tmp);
}
